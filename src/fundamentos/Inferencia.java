package fundamentos;

public class Inferencia {
    public static void main(String[] args) {
        double a = 4.5;

        System.out.println(a);

        var b = 4.5;

        System.out.println(b);

        var c = "teste";
        System.out.println(c);

        c = "Outro teste";
        System.out.println(c);

        double d;
        d = 77.7;
        System.out.println(d);
    }
}
